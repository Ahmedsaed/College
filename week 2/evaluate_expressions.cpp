#include <iostream>

using namespace std;

int main()
{
    // 1- 36/5 = 7
    cout << "36/5= " << 36/5 << endl;

    // 2- 18 - 32 / 6 * 3 = 3
    cout << "18 - 32 / 6 * 3 = " << 18 - 32 / 6 * 3 << endl;

    // 3- 80 % 11 = 3
    cout << "80 % 11 = " << 80 % 11 << endl;

    // 4- 6 - 8 % 11 = -2 
    cout << "6 - 8 % 11 = " << 6 - 8 % 11 << endl;

    // 5- 22.0 / 5 = 4.4
    cout << "22.0/5 = " << 22.0 / 5 << endl;

    // 6- 27 - 12 / 8.0 = 25.5
    cout << "27 - 12 / 8.0 = " << 27 - 12 / 8.0 << endl;

    // 7- 25 - 7 % 3 + 8 / 3 = 26
    cout << "25 - 7 % 3 + 8 / 3 = " << 25 - 7 % 3 + 8 / 3 << endl;

    // 18.0 + 5.0 * 3.0 / 4.0 = 21.75
    cout << "18.0 + 5.0 * 3.0 / 4.0 = " << 18.0 + 5.0 * 3.0 / 4.0 << endl;
}