def depth_limited_search(node, goal, depth_limit):
    if node == goal:
        return [node]  # Goal found, return the path

    if depth_limit == 0:
        return []  # Reached depth limit, return empty path

    for child, distance in romania_map[node].items():
        result = depth_limited_search(child, goal, depth_limit - 1)
        if result:
            return [node] + result  # Append current node to the path and return

    return []  # Goal not found at this depth, return empty path

